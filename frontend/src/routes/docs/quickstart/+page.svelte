<h1>Quick Start Guide</h1>

<p>Get up and running with VibeTable in just a few minutes. This guide walks you through creating your first base, adding data, and sharing with your team.</p>

<h2>Step 1: Sign In</h2>

<p>VibeTable uses <strong>magic link authentication</strong> - no password required!</p>

<ol>
	<li>Go to the login page and enter your email address</li>
	<li>Click "Send Magic Link"</li>
	<li>Check your email for a sign-in link</li>
	<li>Click the link to automatically sign in</li>
</ol>

<div class="tip">
	<strong>Tip:</strong> Magic links expire after 15 minutes for security. If your link has expired, simply request a new one.
</div>

<h2>Step 2: Create Your First Base</h2>

<p>A <strong>base</strong> is like a workbook that contains related tables. Think of it as a container for a project or workflow.</p>

<ol>
	<li>From the home screen, click <strong>"+ New Base"</strong></li>
	<li>Give your base a name (e.g., "Project Tracker" or "Inventory")</li>
	<li>Click <strong>Create</strong></li>
</ol>

<p>Your new base will automatically include a table named "Table 1" with some default fields to get you started.</p>

<h2>Step 3: Add Fields (Columns)</h2>

<p>Fields define the structure of your data. To add a new field:</p>

<ol>
	<li>Click the <strong>+</strong> button at the end of the header row</li>
	<li>Choose a <strong>field type</strong> (text, number, date, etc.)</li>
	<li>Enter a name for your field</li>
	<li>Configure any type-specific options</li>
	<li>Click <strong>Create</strong></li>
</ol>

<p>Learn more about each field type in the <a href="/docs/fields">Fields & Field Types</a> guide.</p>

<h2>Step 4: Add Records (Rows)</h2>

<p>Records are the individual items in your table. To add a record:</p>

<ol>
	<li>Click the <strong>+</strong> button at the bottom of the table, or</li>
	<li>Start typing in the empty row at the bottom</li>
</ol>

<p>You can also:</p>
<ul>
	<li><strong>Click a cell</strong> to edit it directly</li>
	<li><strong>Press Enter</strong> to save and move to the next row</li>
	<li><strong>Press Tab</strong> to move to the next cell</li>
</ul>

