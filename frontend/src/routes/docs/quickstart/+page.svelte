<h2>Step 6: Share Your Base</h2>

<p>Ready to collaborate? Click the <strong>Share</strong> button to:</p>

<ul>
	<li><strong>Invite collaborators</strong> by email with different permission levels</li>
	<li><strong>Create a public link</strong> for view-only access</li>
	<li><strong>Build a form</strong> for collecting data from anyone</li>
</ul>

<p>Learn more in the <a href="/docs/sharing">Sharing & Permissions</a> guide.</p>

